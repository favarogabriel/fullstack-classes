// Não podemos criar constantes com palavras reservadas pela linguagem
// Constantes precisam ter nomes significativos
// Não pode começar o nome de uma constante com um número
// Não podem conter espaços ou traços
// Utilizamos camelCase
// Case-sensitive
// Não podemos modificar o valor de uma constante
// NÃO UTILIZE VAR, UTILIZE CONST.

// - = *  
const primeiroNumero = 5; // Number
const segundoNumero = 10;
const resultado = primeiroNumero * segundoNumero;
const resultadoDuplicado = resultado * 2;

console.log('O resultado inteiro é', resultado);
console.log('O resultado duplicado é', resultadoDuplicado);

console.log('O tipo da linha de código é', typeof primeiroNumero);

/* Dentro de aspas é String, e String é a mesma coisa de texto 
Fora de aspas é Number, e Number é a mesma coisa de número */
